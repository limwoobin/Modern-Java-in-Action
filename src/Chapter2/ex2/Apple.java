package Chapter2.ex2;

import Chapter2.ex1.Color;

public class Apple {
    private Color color;
    private int weight;

    public Apple(Color color , int weight) {
        this.color = color;
        this.weight = weight;
    }

    public Color getColor() {
        return color;
    }

    public int getWeight() {
        return weight;
    }

    @Override
    public String toString() {
        return "Apple{" +
                "color=" + color +
                ", weight=" + weight +
                '}';
    }
}
